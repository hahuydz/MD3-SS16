let Math = +prompt("Mời bạn nhập vào điểm môn thứ nhất: ");
let physics = +prompt("Mời bạn nhập vào điểm môn học thứ hai: ");
let chemistry = +prompt("Mời bạn nhập vào điểm môn học thứ ba: ");
let average = (Math + physics + chemistry) / 3;
document.write(`Điểm trung bình cộng của ba môn là: ${average}`);